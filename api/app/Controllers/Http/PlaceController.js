  /**
   * Display a single place.
   * GET places/:id
   *
   * @param {object} ctx
   * @param {Request} ctx.request
   * @param {Response} ctx.response
   * @param {View} ctx.view
   */
  async show ({ params, response }) {
    try {
      let { id } = params
      id = new ObjectId(id)
      let data = (await Place.query().where({ community_id: id }).fetch()).toJSON()
      response.send(data)
    } catch (error) {
      console.error('index Place: ' + error.name + ': ' + error.message);
    }
  }

